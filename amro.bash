#!/bin/bash

# التنقل داخل المجلد الرئيسي للمشاريع (تأكد من تشغيل السكريبت من داخل مجلد المشاريع)
echo "🔄 جاري بدء عملية تغيير الأسماء..."

# حلقة للمرور على جميع المجلدات الفرعية
for dir in */; do
    # التأكد من أن المسار هو مجلد فعلي
    if [ -d "$dir" ]; then
        # المسار الكامل للملف القديم والجديد
        old_file="${dir}amro.json"
        new_file="${dir}diagram.json"

        # التحقق من وجود ملف diagram.json قبل التغيير
        if [ -f "$old_file" ]; then
            mv "$old_file" "$new_file"
            echo "✅ تم تغيير الاسم في المجلد: ${dir%/}"
        else
            echo "⚠️  تنبيه: الملف غير موجود في المجلد: ${dir%/}"
        fi
    fi
done

echo "🏁 اكتملت المهمة بنجاح يا بشمهندس عمرو."